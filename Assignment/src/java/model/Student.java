package model;

import java.sql.Date;
import java.util.ArrayList;


/**
 *
 * @author DUC
 */
public class Student {
    private int id;
    private String name;
    private String code;
    private boolean gender;
    private Date dob;
    private ArrayList<Group> groups = new ArrayList<>();

    public Student() {
    }

    public Student(int id, String name, String code, boolean gender, Date dob) {
        this.id = id;
        this.name = name;
        this.code = code;
        this.gender = gender;
        this.dob = dob;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public boolean isGender() {
        return gender;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    public ArrayList<Group> getGroups() {
        return groups;
    }

    public void setGroups(ArrayList<Group> groups) {
        this.groups = groups;
    }
    
}
